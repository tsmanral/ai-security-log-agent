def format_timeline_text(timeline: List[Dict[str, Any]]) -> str:
    """
    Format an entity timeline into a grouped readable summary.

    Groups events by source_type, shows timestamps, counts, and transitions.

    Args:
        timeline: Ordered list of event dicts.

    Returns:
        Plain-text formatted timeline summary.
    """
    if not timeline:
        return "_No timeline data available._"

    lines: List[str] = []
    by_source: Dict[str, List[Dict[str, Any]]] = {}
    for event in timeline:
        src = event.get("source_type") or "unknown"
        by_source.setdefault(src, []).append(event)

    for src, events in by_source.items():
        first = events[0].get("timestamp", "")[:16]
        last  = events[-1].get("timestamp", "")[:16]
        types = ", ".join(sorted({e.get("event_type", "") for e in events}))
        lines.append(f"**{src}** ({len(events)} events, {first} → {last}): {types}")

    return "\n".join(lines)


def get_shap_narrative_fragment(shap_values: Optional[Dict[str, float]]) -> str:
    """
    Convert V3 SHAP output into a readable sentence fragment.

    Example output:
        "driven primarily by high login velocity (SHAP: 0.42)"

    Args:
        shap_values: Dict of feature_name → SHAP value. May be None.

    Returns:
        Readable fragment string, or empty string if unavailable.
    """
    if not shap_values:
        return ""
    try:
        top_feat = max(shap_values, key=lambda k: abs(shap_values[k]))
        top_val  = shap_values[top_feat]
        readable = top_feat.replace("_", " ")
        return f"Analysis driven primarily by {readable} (SHAP: {top_val:.2f})."
    except (ValueError, TypeError):
        return ""


def get_confidence_level(
    features: Dict[str, Any],
    rule_alert: Dict[str, Any],
    threat_intel: Optional[Dict[str, Any]],
    timeline: List[Dict[str, Any]],
) -> Tuple[str, str]:
    """
    Determine detection confidence from corroborating signal count.

    HIGH   if >= 3 corroborating signals
    MEDIUM if 2 signals
    LOW    if 1 signal

    [DESIGN CHOICE] Signal-count heuristic is simple to audit and adjust
    without retraining ML models.

    Args:
        features:    Enriched V4 feature dict.
        rule_alert:  V4 rule alert dict.
        threat_intel: Optional AbuseIPDB cache entry.
        timeline:    Ordered entity event history.

    Returns:
        (level: str, plain_english_explanation: str)
    """
    signals: List[str] = []

    if rule_alert.get("rule_weight", 0) >= 0.7:
        signals.append("high-weight rule triggered")
    if threat_intel and threat_intel.get("abuse_score", 0) > 25:
        signals.append("malicious threat intel hit")
    if len(timeline) >= 5:
        signals.append("sustained event timeline (5+ events)")
    if features.get("cross_source_activity"):
        signals.append("cross-source IP corroboration")
    if float(features.get("failure_ratio", 0)) > 0.8:
        signals.append("high failure ratio (>80%)")

    count = len(signals)
    if count >= 3:
        return "HIGH", f"Confirmed by {count} corroborating signals: {'; '.join(signals)}."
    if count == 2:
        return "MEDIUM", f"Supported by {count} signals: {'; '.join(signals)}."
    explanation = signals[0] if signals else "single rule trigger with no corroboration"
    return "LOW", f"Single signal: {explanation}."


