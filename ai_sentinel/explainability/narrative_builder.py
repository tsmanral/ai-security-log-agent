def _narrate_large_data_transfer(
    features: Dict[str, Any],
    rule_alert: Dict[str, Any],
    timeline: Optional[List[Dict[str, Any]]],
) -> str:
    """Build large data transfer / exfiltration narrative."""
    device    = features.get("device_id", "Unknown")
    bytes_out = float(features.get("total_bytes_out", 0))
    dest_ip   = features.get("dest_ip") or "unknown destination"
    mb        = bytes_out / 1_000_000

    sentence = (
        f"Host {device} transferred {mb:.1f} MB outbound to {dest_ip} "
        f"over 30 minutes, exceeding the normal baseline threshold."
    )
    sentence += " Consistent with MITRE T1048 (Exfiltration)."
    return sentence


