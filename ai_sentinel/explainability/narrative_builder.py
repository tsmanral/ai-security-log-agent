def generate_alert_narrative(
    features: Dict[str, Any],
    rule_alert: Dict[str, Any],
    shap_values: Optional[Dict[str, float]] = None,
    entity_timeline: Optional[List[Dict[str, Any]]] = None,
) -> str:
    """
    Generate a 2-3 sentence alert narrative from V4 features and rule output.

    Gracefully degrades — if generation fails, returns a fallback string.

    [MYTHOS ALIGNMENT — human-readable incident narrative]
    [DESIGN CHOICE] Pure Python, zero external dependencies.

    Args:
        features:        Enriched V4 feature dict for the triggering event.
        rule_alert:      V4 rule alert dict (type, severity, mitre_id, etc.).
        shap_values:     Optional per-feature SHAP values from V3 explainability.
        entity_timeline: Optional ordered event history for the entity.

    Returns:
        Human-readable alert narrative string.
    """
    try:
        rule_type = rule_alert.get("type", "UNKNOWN")
        dispatcher = {
            "BRUTE_FORCE":       _narrate_brute_force,
            "CREDENTIAL_STUFFING": _narrate_credential_stuffing,
            "LOW_AND_SLOW":      _narrate_low_and_slow,
            "LATERAL_MOVEMENT":  _narrate_lateral_movement,
            "PORT_SCAN":         _narrate_port_scan,
            "LARGE_DATA_TRANSFER": _narrate_large_data_transfer,
            "SUSPICIOUS_PROCESS": _narrate_suspicious_process,
            "PRIVILEGE_ESCALATION": _narrate_privilege_escalation,
            "PERSISTENCE":       _narrate_persistence,
        }
        func = dispatcher.get(rule_type, _narrate_generic)
        narrative = func(features, rule_alert, entity_timeline)
        shap_frag = get_shap_narrative_fragment(shap_values)
        if shap_frag:
            narrative += f" {shap_frag}"
        return narrative
    except Exception:
        logger.exception("generate_alert_narrative failed — using fallback.")
        return f"Narrative unavailable: {rule_alert.get('reason', 'unknown reason')}"


def _narrate_brute_force(
    features: Dict[str, Any],
    rule_alert: Dict[str, Any],
    timeline: Optional[List[Dict[str, Any]]],
) -> str:
    """Build brute force narrative."""
    ip          = features.get("source_ip", "Unknown IP")
    failed_5min = int(features.get("failed_logins_last_5min", 0))
    failed_15min= int(features.get("failed_logins_last_15min", 0))
    velocity    = float(features.get("login_attempt_velocity", 0))
    uniq_users  = int(features.get("unique_usernames_per_ip", 0))

    sentence = (
        f"IP {ip} performed {failed_5min} failed login attempts in the last "
        f"5 minutes at {velocity:.1f} attempts/minute."
    )

    if failed_15min > failed_5min * 1.5:
        sentence += " Attack is accelerating over the 15-minute observation window."

    if uniq_users > 1:
        sentence += f" Targeting {uniq_users} distinct accounts."

    sentence += " Consistent with MITRE T1110.001 (Password Guessing)."
    return sentence


def _narrate_credential_stuffing(
    features: Dict[str, Any],
    rule_alert: Dict[str, Any],
    timeline: Optional[List[Dict[str, Any]]],
) -> str:
    """Build credential stuffing narrative."""
    ip          = features.get("source_ip", "Unknown IP")
    uniq_users  = int(features.get("unique_usernames_per_ip", 0))
    fail_ratio  = float(features.get("failure_ratio", 0))
    cross       = bool(features.get("cross_source_activity", False))

    sentence = (
        f"IP {ip} attempted logins against {uniq_users} distinct usernames "
        f"with a {fail_ratio * 100:.0f}% failure rate."
    )
    if cross:
        sentence += " This IP is also observed in network flow data."
    sentence += " Consistent with MITRE T1110.004 (Credential Stuffing)."
    return sentence


def _narrate_low_and_slow(
    features: Dict[str, Any],
    rule_alert: Dict[str, Any],
    timeline: Optional[List[Dict[str, Any]]],
) -> str:
    """Build low-and-slow attack narrative."""
    ip       = features.get("source_ip", "Unknown IP")
    velocity = float(features.get("login_attempt_velocity", 0))
    window   = 15

    sentence = (
        f"IP {ip} conducted a slow distributed attack across {window} minutes "
        f"maintaining {velocity:.2f} attempts/minute to evade detection thresholds."
    )
    if timeline and len(timeline) >= 2:
        first_ts = timeline[0].get("timestamp", "")[:16]
        last_ts  = timeline[-1].get("timestamp", "")[:16]
        if first_ts and last_ts:
            sentence += f" Activity spans from {first_ts} to {last_ts}."
    sentence += " Consistent with MITRE T1110.003 (Password Spraying)."
    return sentence


def _narrate_lateral_movement(
    features: Dict[str, Any],
    rule_alert: Dict[str, Any],
    timeline: Optional[List[Dict[str, Any]]],
) -> str:
    """Build lateral movement narrative."""
    ip              = rule_alert.get("ip") or features.get("source_ip", "Unknown IP")
    unique_devices  = features.get("unique_ips_per_username", 0)
    unique_users    = features.get("unique_usernames_per_ip", 0)
    affected_users  = rule_alert.get("affected_users", "")

    sentence = (
        f"Source IP {ip} accessed {unique_devices} devices and "
        f"{unique_users} accounts suggesting lateral movement."
    )
    if affected_users:
        users_list = ", ".join(affected_users.split(",")[:5])
        sentence += f" Affected accounts include: {users_list}."
    sentence += " Consistent with MITRE T1021 (Remote Services)."
    return sentence


