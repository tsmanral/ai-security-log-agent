def analyze_false_positive(
    original_alert: Dict[str, Any],
    analyst_note: str = "",
) -> Dict[str, Any]:
    """
    Analyse an alert for known false-positive patterns via pure decision tree.

    [MYTHOS ALIGNMENT — adaptive feedback reasoning]
    [DESIGN CHOICE] Pure Python decision tree, no LLM required.

    Examines feature values against known FP patterns:
      - Low-velocity single-account (monitoring/automation)
      - Large overnight transfer (backup job)
      - LOLBin in backup/admin tool cmdline
      - CI/CD burst logins

    Args:
        original_alert: Full alert dict (should include features, rule_type, etc.).
        analyst_note:   Optional free-text note from the analyst.

    Returns:
        Dict with pattern, suggested_threshold_change, missing_context, confidence.
    """
    features   = original_alert.get("features", {})
    rule_type  = original_alert.get("type", "UNKNOWN")
    velocity   = float(features.get("login_attempt_velocity", 0))
    uniq_users = float(features.get("unique_usernames_per_ip", 0))
    bytes_out  = float(features.get("total_bytes_out", 0))
    lolbin     = float(features.get("lolbin_usage_count", 0))
    failed_5m  = float(features.get("failed_logins_last_5min", 0))
    device     = features.get("device_id", "")

    note_lower = analyst_note.lower()

    # --- Pattern 1: Monitoring/automation burst (low-velocity, single user) ---
    if (rule_type == "BRUTE_FORCE" and uniq_users <= 1 and velocity < 0.5):
        return _fp_result(
            "monitoring_automation",
            {"failed_logins_last_5min": max(int(failed_5m * 1.5), 10)},
            "Service account / monitoring tool generating repeated auth checks.",
            "MEDIUM",
        )

    # --- Pattern 2: Large overnight backup transfer ---
    if (rule_type == "LARGE_DATA_TRANSFER" and bytes_out > 10_000_000):
        hour = _get_event_hour(original_alert)
        if hour is not None and (hour < 6 or hour >= 22):
            return _fp_result(
                "backup_job",
                {"total_bytes_out": int(bytes_out * 1.25)},
                "Overnight large transfer — likely scheduled backup job.",
                "HIGH",
            )

    # --- Pattern 3: LOLBin in admin/backup tooling ---
    if (rule_type == "SUSPICIOUS_PROCESS" and lolbin > 0):
        if any(kw in note_lower for kw in ("backup", "admin", "deploy", "sccm", "ansible")):
            return _fp_result(
                "admin_tooling_lolbin",
                {"lolbin_usage_count": 2},
                "LOLBin used by known admin/backup tooling — whitelist candidate.",
                "HIGH",
            )

    # --- Pattern 4: CI/CD burst logins ---
    if (rule_type in ("BRUTE_FORCE", "CREDENTIAL_STUFFING")
            and any(kw in note_lower for kw in ("ci", "cd", "jenkins", "github", "deploy"))):
        return _fp_result(
            "cicd_burst",
            {"failed_logins_last_5min": int(failed_5m * 2)},
            "CI/CD pipeline authentication burst — automated deployment detected.",
            "MEDIUM",
        )

    # --- Default: no recognised FP pattern ---
    return _fp_result(
        "no_known_pattern",
        {},
        "No common false-positive pattern matched. Manual review required.",
        "LOW",
    )


def _fp_result(
    pattern: str,
    suggested_thresholds: Dict[str, Any],
    missing_context: str,
    confidence: str,
) -> Dict[str, Any]:
    """Build a standardised false-positive analysis result dict."""
    return {
        "pattern":                    pattern,
        "suggested_threshold_change": suggested_thresholds,
        "missing_context":            missing_context,
        "confidence":                 confidence,
    }


def _get_event_hour(alert: Dict[str, Any]) -> Optional[int]:
    """Extract the hour from an alert's timestamp if available."""
    ts = alert.get("timestamp") or alert.get("created_at", "")
    if not ts:
        return None
    try:
        return datetime.fromisoformat(str(ts)[:19]).hour
    except ValueError:
        return None
