def generate_investigative_summary(
    ip: str,
    incident_data: Dict[str, Any],
    entity_timeline: List[Dict[str, Any]],
    similar_past_incidents: List[Dict[str, Any]],
    threat_intel: Optional[Dict[str, Any]],
) -> str:
    """
    Generate a full multi-paragraph investigation report for an IP.

    [MYTHOS ALIGNMENT — structured investigation report]
    [GLASSWING ALIGNMENT — analyst case file generation]
    [DESIGN CHOICE] Pure logic — no external dependencies.

    Args:
        ip:                      Source IP under investigation.
        incident_data:           Incident record dict from DB.
        entity_timeline:         Ordered event history for the IP.
        similar_past_incidents:  Previous incidents matching the IP/type.
        threat_intel:            Optional AbuseIPDB cache entry.

    Returns:
        Full investigation report as a formatted markdown string.
    """
    try:
        sections = [
            _para_executive_summary(ip, incident_data, entity_timeline, threat_intel),
            _para_attack_pattern(ip, incident_data, entity_timeline),
            _para_affected_assets(entity_timeline),
            _para_confidence(incident_data, entity_timeline, threat_intel),
            _para_recommendations(incident_data),
        ]
        if similar_past_incidents:
            sections.append(_para_historical_context(similar_past_incidents))
        return "\n\n---\n\n".join(filter(None, sections))
    except Exception:
        logger.exception("generate_investigative_summary failed for IP %s", ip)
        return f"**Investigation report unavailable for IP {ip}.**"


def _para_executive_summary(
    ip: str,
    incident: Dict[str, Any],
    timeline: List[Dict[str, Any]],
    threat_intel: Optional[Dict[str, Any]],
) -> str:
    """Para 1: Executive summary."""
    attack    = incident.get("attack_type", "Unknown Attack")
    severity  = incident.get("severity_label", "UNKNOWN")
    first     = incident.get("first_seen", "")[:16]
    last      = incident.get("last_seen",  "")[:16]
    cnt       = incident.get("anomaly_count", len(timeline))

    ti_note = ""
    if threat_intel:
        score = threat_intel.get("abuse_score", 0)
        country = threat_intel.get("country_code", "")
        ti_note = f" Threat intelligence: AbuseIPDB score {score}/100 (origin: {country})."

    return (
        f"## 📋 Executive Summary\n\n"
        f"**IP Address**: `{ip}` | **Attack Type**: {attack} | **Severity**: {severity}\n\n"
        f"This incident was first detected at **{first}** and was last observed at **{last}**, "
        f"generating {cnt} anomaly event(s).{ti_note}"
    )


def _para_attack_pattern(
    ip: str, incident: Dict[str, Any], timeline: List[Dict[str, Any]]
) -> str:
    """Para 2: Attack pattern analysis."""
    if not timeline:
        return "## 🔍 Attack Pattern\n\nInsufficient timeline data for pattern analysis."

    event_types = [e.get("event_type", "") for e in timeline]
    unique_types = list(dict.fromkeys(event_types))  # ordered unique

    timeline_text = format_timeline_text(timeline)
    return (
        f"## 🔍 Attack Pattern\n\n"
        f"Observed event sequence: `{'` → `'.join(unique_types[:8])}`\n\n"
        f"{timeline_text}"
    )


def _para_affected_assets(timeline: List[Dict[str, Any]]) -> str:
    """Para 3: Affected assets derived from timeline."""
    devices   = list({e.get("device_id", "") for e in timeline if e.get("device_id")})
    usernames = list({e.get("effective_username", "") for e in timeline if e.get("effective_username")})

    return (
        f"## 🖥️ Affected Assets\n\n"
        f"**Devices ({len(devices)})**: {', '.join(devices[:10]) or 'None identified'}\n\n"
        f"**Accounts ({len(usernames)})**: {', '.join(usernames[:10]) or 'None identified'}"
    )


def _para_confidence(
    incident: Dict[str, Any],
    timeline: List[Dict[str, Any]],
    threat_intel: Optional[Dict[str, Any]],
) -> str:
    """Para 4: Confidence analysis."""
    level, explanation = get_confidence_level(
        features={},
        rule_alert=incident,
        threat_intel=threat_intel,
        timeline=timeline,
    )
    badge = {"HIGH": "🟢 HIGH", "MEDIUM": "🟡 MEDIUM", "LOW": "🔴 LOW"}.get(level, level)
    return f"## 🎯 Detection Confidence\n\n**{badge}** — {explanation}"


def _para_recommendations(incident: Dict[str, Any]) -> str:
    """Para 5: Recommended actions based on attack type."""
    attack = incident.get("attack_type", "")
    recs = _get_recommendations(attack)
    lines = "\n".join(f"- {r}" for r in recs)
    return f"## ✅ Recommended Actions\n\n{lines}"


def _para_historical_context(past: List[Dict[str, Any]]) -> str:
    """Optional: Historical incident context."""
    count = len(past)
    types = list({i.get("attack_type", "") for i in past})[:5]
    return (
        f"## 📅 Historical Context\n\n"
        f"{count} related past incident(s) found. Previous attack types: "
        f"{', '.join(types) or 'N/A'}."
    )


